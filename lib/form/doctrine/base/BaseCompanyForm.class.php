      new sfValidatorDoctrineUnique(array('model' => 'Company', 'column' => array('company_cif')))
    );

    $this->widgetSchema->setNameFormat('company[%s]');

    $this->errorSchema = new sfValidatorErrorSchema($this->validatorSchema);

